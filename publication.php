              <h3>In search of Quality: A study on elementary education in India </a></h3>
            </header>
            <section class="content">
                <h4>Abstract</h4>
                    <p align="justify">This study investigates the quality of education attained by students in rural areas of different states in India. The government of India has focused on fulfillment of the Millennium Development Goals, children should be able to attain at least primary education, neglecting learning achievement. We examine quality of education by computing an index, Learning Achievement Index (LAI) based on data from Annual Status of Education Report(ASER). We compute two more indices based on quality of infrastructure provided and quality of teachers and ranked the states accordingly. We look for the impact of expenditure by the Indian Government in quality provisions on the Learning Achievement Index(LAI). The results show certain positive and negative impacts, most importantly showing that mother’s education has a significant impact on the child’s learning achievement. Some of the results support our expectations and we explain why some of our hypothesis was invalidated by the LAI based analysis.</p>
